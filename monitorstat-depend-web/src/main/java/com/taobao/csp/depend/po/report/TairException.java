package com.taobao.csp.depend.po.report;

public class TairException {

	private long curExceptionNum;
	private long oldExceptionNum;
	private String tair_group_name;
	
	public long getCurExceptionNum() {
		return curExceptionNum;
	}
	public void setCurExceptionNum(long curExceptionNum) {
		this.curExceptionNum = curExceptionNum;
	}
	public long getOldExceptionNum() {
		return oldExceptionNum;
	}
	public void setOldExceptionNum(long oldExceptionNum) {
		this.oldExceptionNum = oldExceptionNum;
	}
	public String getTair_group_name() {
		return tair_group_name;
	}
	public void setTair_group_name(String tair_group_name) {
		this.tair_group_name = tair_group_name;
	}
}
