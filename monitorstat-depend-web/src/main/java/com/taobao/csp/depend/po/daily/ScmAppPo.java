package com.taobao.csp.depend.po.daily;

public class ScmAppPo {

	private String machines;
	private String ips;
	private String appName;
	private String desc;
	private String opsOwner;
	public String getMachines() {
		return machines;
	}
	public void setMachines(String machines) {
		this.machines = machines;
	}
	public String getIps() {
		return ips;
	}
	public void setIps(String ips) {
		this.ips = ips;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getOpsOwner() {
		return opsOwner;
	}
	public void setOpsOwner(String opsOwner) {
		this.opsOwner = opsOwner;
	}
}
