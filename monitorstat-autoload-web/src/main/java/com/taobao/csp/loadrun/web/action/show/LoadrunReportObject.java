package com.taobao.csp.loadrun.web.action.show;

public class LoadrunReportObject {
	
	private String resource;
	
	private String totalPv;
	
	private String avgRt;
	
	private String resourceRatio;
	
	private String maxRt;
	
	private String minRt;
	
	private String waveRatio;

	public String getResource() {
		return resource;
	}

	public void setResource(String resource) {
		this.resource = resource;
	}

	public String getTotalPv() {
		return totalPv;
	}

	public void setTotalPv(String totalPv) {
		this.totalPv = totalPv;
	}

	public String getAvgRt() {
		return avgRt;
	}

	public void setAvgRt(String avgRt) {
		this.avgRt = avgRt;
	}

	public String getResourceRatio() {
		return resourceRatio;
	}

	public void setResourceRatio(String resourceRatio) {
		this.resourceRatio = resourceRatio;
	}

	public String getMaxRt() {
		return maxRt;
	}

	public void setMaxRt(String maxRt) {
		this.maxRt = maxRt;
	}

	public String getMinRt() {
		return minRt;
	}

	public void setMinRt(String minRt) {
		this.minRt = minRt;
	}

	public String getWaveRatio() {
		return waveRatio;
	}

	public void setWaveRatio(String waveRatio) {
		this.waveRatio = waveRatio;
	}
	
}
